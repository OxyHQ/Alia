/**
 * Alia logo lockup — brand flower + "Alia" script wordmark. The wordmark
 * follows the theme foreground; the flower keeps the brand purple.
 */
export function AliaLogo({ width, height, color }: AliaLogoProps) {
  const { colors } = useColorScheme();
  const wordFill = color ?? colors.foreground;

  // Resolve dimensions preserving aspect ratio
  let w: number;
  let h: number;
  if (width && height) {
    w = width;
    h = height;
  } else if (width) {
    w = width;
    h = width / ASPECT;
  } else if (height) {
    h = height;
    w = height * ASPECT;
  } else {
    w = 160;
    h = 160 / ASPECT;
  }

  return (
    <Svg width={w} height={h} viewBox={VIEWBOX}>
      <Path d={FLOWER_PATH} fill={BRAND_FILL} transform={FLOWER_TRANSFORM} />
      <G transform={WORD_TRANSFORM} fill={wordFill}>
        <Path d={WORD_PATH_1} />
        <Path d={WORD_PATH_2} />
      </G>
    </Svg>
  );
}
